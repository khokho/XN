package ge.exen.models;


public class Chat {

  private long chatId;
  private long from;
  private long to;


  public long getChatId() {
    return chatId;
  }

  public void setChatId(long chatId) {
    this.chatId = chatId;
  }


  public long getFrom() {
    return from;
  }

  public void setFrom(long from) {
    this.from = from;
  }


  public long getTo() {
    return to;
  }

  public void setTo(long to) {
    this.to = to;
  }

}
